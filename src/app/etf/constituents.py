def get_constituents(underlying_ticker: str) -> list[tuple[str, float]]:
    """Return constituent (ticker, weight%) pairs for an underlying ETF."""
    return CONSTITUENTS.get(underlying_ticker.upper(), [])


def get_all_constituent_tickers() -> set[str]:
    """Return the set of all unique constituent tickers across all ETFs."""
    tickers: set[str] = set()
    for holdings in CONSTITUENTS.values():
        for ticker, _ in holdings:
            tickers.add(ticker)
    return tickers
